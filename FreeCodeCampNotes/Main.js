// start with let, const, or var to create a variable
let character = "Hello"; //I create a variable(character) with the value (hello)
console.log(character); // display what the value(hello) is for the variable (character)
character = "World"; //forces character to change from hello to world

let secondCharacter; //I create a variable(secondCharacter) - there is no value
secondCharacter = "Test"; //I have a variable(secondCharacter) without a value, I set the value to be test
console.log(secondCharacter); //display the value(test) of secondCharacter(variable)
